  geom_area(aes(y=(Barley+Wheat)/1000000, fill="Fina total")) + 
  geom_area(aes(y=Wheat/1000000, fill="Trigo")) + 
  geom_area(aes(y=Barley/1000000, fill="Cebada")) + 
  scale_fill_manual(name="", 
                    values = c("Fina total"="#8A8A8A", 
                               "Trigo" = "#3CB371", 
                               "Cebada" = "#CD6839"),
                    breaks = c("Fina total", "Trigo", "Cebada")) + 
  scale_y_continuous(breaks = seq(0,8, by = 1))+
  scale_x_continuous(breaks = seq(1970, max(da$Year), by = 5))+
  labs(y = "Millones de has", x = NULL, fill=NULL,
       title = "Evoluci√≥n de superficie de cereales de Invierno en Argentina")+
  theme(panel.grid.minor = element_blank())

